package Main;

public class ciclos {
    public static void main(String[] args) {
        // ciclo while

        byte n= 1;

        while (n <= 10){
            System.out.println(n);
            n++;
        }


        //ciclo do while

        //ciclo for
        for (int i = 0 ; i < 15 ; i++){
            System.out.println("*");
        }

        for (int j = 1; j <= 15; j++){
            System.out.println(j);
        }
    }
}
